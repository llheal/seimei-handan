/**
 * 三才配置の吉凶を取得する
 */
export function getSansaiFortune(tenElement, jinElement, chiElement) {
    const key = `${tenElement}-${jinElement}-${chiElement}`;
    return SANSAI_TABLE[key] || { rating: '吉凶混合', desc: 'この配置の詳細な判定は個別のご相談をお勧めいたします。' };
}

/**
 * 吉凶レベルの数値化 (スコアリング用)
 */
export function fortuneScore(rating) {
    switch (rating) {
        case '大吉': return 5;
        case '吉': return 4;
        case '吉凶混合': return 3;
        case '凶': return 2;
        case '大凶': return 1;
        default: return 0;
    }
}

/**
 * 吉凶レベルのCSSクラス名
 */
export function fortuneClass(rating) {
    switch (rating) {
        case '大吉': return 'fortune-daikichi';
        case '吉': return 'fortune-kichi';
        case '吉凶混合': return 'fortune-mixed';
        case '凶': return 'fortune-kyo';
        case '大凶': return 'fortune-daikyo';
        default: return 'fortune-unknown';
    }
}
