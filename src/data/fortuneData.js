/**
 * 画数から吉凶を取得する（81を超える場合は80で割った余りで判定）
 */
export function getStrokeFortune(strokes) {
    if (strokes <= 0) return { rating: '---', desc: '画数が不正です。' };
    const adjusted = strokes > 81 ? ((strokes - 1) % 80) + 1 : strokes;
    return STROKE_FORTUNE[adjusted] || { rating: '---', desc: '' };
}

/**
 * 五行変換: 画数の下一桁から五行を決定
 * 1,2→木  3,4→火  5,6→土  7,8→金  9,0→水
 */
export function strokeToElement(strokes) {
    const lastDigit = strokes % 10;
    if (lastDigit === 1 || lastDigit === 2) return '木';
    if (lastDigit === 3 || lastDigit === 4) return '火';
    if (lastDigit === 5 || lastDigit === 6) return '土';
    if (lastDigit === 7 || lastDigit === 8) return '金';
    return '水'; // 9, 0
}

