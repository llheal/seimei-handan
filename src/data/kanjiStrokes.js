/**
 * 文字列全体の画数配列を取得する
 * @param {string} name - 漢字文字列
 * @returns {Array<{char: string, strokes: number|null}>}
 */
export function getNameStrokes(name) {
    if (!name) return [];
    return Array.from(name).map(char => ({
        char,
        strokes: getStrokeCount(char)
    }));
}

/**
 * 文字列全体の合計画数を計算する
 * @param {string} name - 漢字文字列
 * @returns {number} 合計画数
 */
export function getTotalStrokes(name) {
    if (!name) return 0;
    return Array.from(name).reduce((sum, char) => {
        const s = getStrokeCount(char);
        return sum + (s || 0);
    }, 0);
}

