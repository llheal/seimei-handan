/**
 * 漢字の画数を取得する
 * @param {string} char - 単一の漢字文字
 * @returns {number|null} 画数 (見つからない場合はnull)
 */
export function getStrokeCount(char) {
    if (!char || char.length === 0) return null;
    const c = char.charAt(0);

    // データベースから検索
    if (KANJI_STROKES[c] !== undefined) {
        return KANJI_STROKES[c];
    }

    // CJK統合漢字のUnicode範囲チェック
    const code = c.charCodeAt(0);
    if (code >= 0x4E00 && code <= 0x9FFF) {
        // データベースにない漢字: UnicodeブロックベースのざっくりEstimate
        // CJK Unified Ideographsは大まかに画数順に並んでいる
        // 正確ではないが0画よりはるかに良い
        const ratio = (code - 0x4E00) / (0x9FFF - 0x4E00);
        const estimated = Math.round(4 + ratio * 16); // 4〜20画の範囲
        console.warn(`漢字「${c}」はデータベースにありません。推定${estimated}画として計算します。`);
        return estimated;
    }

    // ひらがな (各文字の一般的な画数)
    if (code >= 0x3040 && code <= 0x309F) {
        return getHiraganaStrokes(c);
    }

    // カタカナ
    if (code >= 0x30A0 && code <= 0x30FF) {
        return getKatakanaStrokes(c);
    }

    return null;
}

